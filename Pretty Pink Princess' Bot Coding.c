task Catapult()
{
	while(true)
	{
		if(vexRT[Btn6U] == 1 && vexRT[Btn6D] == 0)
		{
			motor[TowerLeft] = 127;
			motor[TowerRight] = 127;
		}
		else if(vexRT[Btn6U] == 0 && vexRT[Btn6D] == 1)
		{
			motor[TowerLeft] = -127;
			motor[TowerRight] = -127;
		}
		else if(vexRT[Btn6U] == 0 && vexRT[Btn6D] == 0)
		{
			motor[TowerLeft] = 0;
			motor[TowerRight] = 0;
		}
	}
}
